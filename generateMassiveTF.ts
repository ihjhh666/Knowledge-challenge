const rawPool: any[] = [];
const uniqueCheck = new Set();

function add(cat: string, text: string, isTrue: boolean) {
   const q = text.trim().replace(/\s+/g, ' '); // Clean whitespaces
   if(uniqueCheck.has(q)) return;
   uniqueCheck.add(q);
   rawPool.push({ category: cat, question: q, isTrue });
}

function mix(cat: string, items: any[], ft: (a:any)=>string, ff: (a:any, b:any)=>string) {
  for(let i=0; i<items.length; i++) {
     add(cat, ft(items[i]), true);
     for(let j=0; j<items.length; j++) {
        if(i!==j) add(cat, ff(items[i], items[j]), false);
     }
  }
}


const TARGET_LIMIT = 150; // Guaranteed equal distribution max
let finalPool = [];
const statsReport = {
  totalOriginalGenerated: rawPool.length,
  totalFinalSelected: 0,
  deletedDueToLimit: 0,
  targetPerCategory: TARGET_LIMIT,
  cats: {} as any,
  diff: {easy:0, medium:0, hard:0}
};

Object.keys(grouped).forEach(cat => {
  let list = grouped[cat];
  list.sort(() => Math.random() - 0.5); // Shuffle strongly
  
  if (list.length > TARGET_LIMIT) {
     statsReport.deletedDueToLimit += (list.length - TARGET_LIMIT);
     list = list.slice(0, TARGET_LIMIT);
  }
  
  const L = list.length;
  // Fallbacks for missing generated data: if L < 50, so be it, but we generated a LOT.
  
  const easyC = Math.floor(L * 0.40);
  const medC = Math.floor(L * 0.40);
  
  list.forEach((q, idx) => {
      if (idx < easyC) q.difficulty = 'easy';
      else if (idx < easyC + medC) q.difficulty = 'medium';
      else q.difficulty = 'hard';
      
      finalPool.push(q);
      statsReport.diff[q.difficulty]++;
  });
  
  statsReport.cats[cat] = L;
});

statsReport.totalFinalSelected = finalPool.length;

